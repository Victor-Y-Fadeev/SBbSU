package sem3.hw4.task1;

/** Interface for objects with coordinates. */
public interface Coordinate {
    /** Get X coordinate. */
    public int getX();
    /** Get Y coordinate. */
    public int getY();
    /** Get Fi. */
    public int getFi();
    /** Set X coordinate. */
    public void setX(int x);
    /** Set Y coordinate. */
    public void setY(int y);
    /** Set fi. */
    public void setFi(int fi);
}
